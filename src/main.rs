


fn main() {

    //Integer

    let x: i32 = -42;
    let y: u64 = 65;
    println!("Signed Integer:{}", x);
    println!("Unsigned Integer: {}", y);

    //Floats
    let pi: f32 = 3.14;
    println!("Value of pi: {}", pi);

    //Boolean

    let is_snwoing: bool = true;
    println!("Is it snowing?: {}", is_snwoing);

    // Character type

    let letter: char = 'a';
    println!("First letter of the alphabet is: {}",letter);
}
